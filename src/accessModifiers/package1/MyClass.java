package accessModifiers.package1;

public class MyClass {
	
	public void publicVoidMyClass() {
		System.out.println("publicVoidMyClass");
	}
	
	protected void protectedVoidMyClass() {
		System.out.println("protectedVoidMyClass");
	}
	
	private void privateVoidMyClass() {
		System.out.println("privateVoidMyClass");
	}
	
	void defaultVoidMyClass() {
		System.out.println("defaultVoidMyClass");
	}	

}
